CREATE TABLE tfg.gimt_town (
  id_town int NOT NULL AUTO_INCREMENT COMMENT 'Town Identifier',
  id_t_province int NOT NULL COMMENT 'Province Identifier',
  town_name varchar(100) NOT NULL DEFAULT '' COMMENT 'Town name',
  id_t_jd int NOT NULL COMMENT 'Judicial District Identifier',
  PRIMARY KEY (id_town),
  UNIQUE KEY id_town_UNIQUE (id_town),
  KEY id_t_province_FK_idx (id_t_province),
  CONSTRAINT id_t_province_FK FOREIGN KEY (id_t_province) REFERENCES gimt_province (id_province) ON DELETE NO ACTION ON UPDATE NO ACTION,
  KEY id_t_jd_FK_idx (id_t_jd),
  CONSTRAINT id_t_jd_FK FOREIGN KEY (id_t_jd) REFERENCES gimt_judicial_district (id_jd) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='Distinct Towns in Euskadi';

